import getSkyline from './218天际线问题';
import totalCost from './2462雇佣K位工人的总代价';

test('218. 天际线问题', () => {
	expect(
		getSkyline([
			[2, 9, 10],
			[3, 7, 15],
			[5, 12, 12],
			[15, 20, 10],
			[19, 24, 8],
		]),
	).toEqual([
		[2, 10],
		[3, 15],
		[7, 12],
		[12, 0],
		[15, 10],
		[20, 8],
		[24, 0],
	]);
});

test('2462. 雇佣 K 位工人的总代价', () => {
	expect(totalCost([17, 12, 10, 2, 7, 2, 11, 20, 8], 3, 4)).toBe(11);
});
